#pragma once
#include <windows.h>

// 128-byte base EDID so Windows treats MultiBox monitors like a normal HDMI display
// (friendly name, timings, Display Settings identify). Modes are still reported via
// ParseMonitorDescription from DisplayPresets.

namespace WindowDisplay::Edid
{
    constexpr UINT32 kSize = 128;

    // Derived from Microsoft IddSample / Dell S2719DGF block, renamed for MultiBox Viewer.
    inline const BYTE kTemplate[kSize] = {
        0x00,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0x00,0x10,0xAC,0xE6,0xD0,0x55,0x5A,0x4A,0x30,0x24,0x1D,0x01,
        0x04,0xA5,0x3C,0x22,0x78,0xFB,0x6C,0xE5,0xA5,0x55,0x50,0xA0,0x23,0x0B,0x50,0x54,0x00,0x02,0x00,
        0xD1,0xC0,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x58,0xE3,0x00,
        0xA0,0xA0,0xA0,0x29,0x50,0x30,0x20,0x35,0x00,0x55,0x50,0x21,0x00,0x00,0x1A,0x00,0x00,0x00,0xFF,
        0x00,0x4D,0x42,0x56,0x2D,0x30,0x30,0x30,0x30,0x0A,0x20,0x20,0x20,0x20,0x00,0x00,0x00,0xFC,0x00,
        0x4D,0x75,0x6C,0x74,0x69,0x42,0x6F,0x78,0x0A,0x20,0x20,0x20,0x20,0x00,0x00,0x00,0xFD,0x00,0x28,
        0x9B,0xFA,0xFA,0x40,0x01,0x0A,0x20,0x20,0x20,0x20,0x20,0x20,0x00,0x00
    };

    inline void FixChecksum(BYTE edid[kSize])
    {
        BYTE sum = 0;
        for (UINT32 i = 0; i < kSize - 1; ++i)
            sum = static_cast<BYTE>(sum + edid[i]);
        edid[kSize - 1] = static_cast<BYTE>(0x100 - sum);
    }

    inline void Build(BYTE edid[kSize], UINT32 connectorIndex)
    {
        CopyMemory(edid, kTemplate, kSize);
        // Unique serial / product instance per connector (bytes 12-15).
        edid[12] = static_cast<BYTE>(0x40 + (connectorIndex & 0x3F));
        edid[13] = 0x4D; // M
        edid[14] = 0x42; // B
        edid[15] = static_cast<BYTE>(0x30 + (connectorIndex % 10));
        // Serial string descriptor "MBV-000n"
        edid[0x4F] = static_cast<BYTE>('0' + ((connectorIndex / 100) % 10));
        edid[0x50] = static_cast<BYTE>('0' + ((connectorIndex / 10) % 10));
        edid[0x51] = static_cast<BYTE>('0' + (connectorIndex % 10));
        FixChecksum(edid);
    }

    inline bool IsOurs(const BYTE* data, UINT32 size)
    {
        if (!data || size < kSize) return false;
        if (data[0] != 0x00 || data[1] != 0xFF) return false;
        // Detailed timing descriptors begin at offset 54; look for monitor-name tag 0xFC.
        for (UINT32 i = 54; i + 18 <= kSize; i += 18)
        {
            if (data[i] == 0x00 && data[i + 1] == 0x00 && data[i + 2] == 0x00 && data[i + 3] == 0xFC &&
                data[i + 5] == 'M' && data[i + 6] == 'u' && data[i + 7] == 'l' &&
                data[i + 8] == 't' && data[i + 9] == 'i' && data[i + 10] == 'B')
            {
                return true;
            }
        }
        return false;
    }
}
